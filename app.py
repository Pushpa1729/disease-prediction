# ========== PDF GENERATOR ==========
def generate_pdf(patient_name, disease, result, inputs, input_labels):
    result = result.replace("⚠️", "WARNING:").replace("✅", "OK:")
    pdf = FPDF()
    pdf.add_page()
    pdf.set_fill_color(14, 17, 23)

    # Header
    pdf.set_font("Arial", "B", 22)
    pdf.set_text_color(44, 130, 201)
    pdf.cell(0, 15, "Multiple Disease Prediction System", ln=True, align="C")
    pdf.set_font("Arial", "", 11)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 8, "AI-Powered Health Report", ln=True, align="C")
    pdf.ln(5)

    # Divider
    pdf.set_draw_color(44, 130, 201)
    pdf.set_line_width(0.8)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(6)

    # Patient Info
    pdf.set_font("Arial", "B", 13)
    pdf.set_text_color(44, 130, 201)
    pdf.cell(0, 10, "Patient Information", ln=True)
    pdf.set_font("Arial", "", 12)
    pdf.set_text_color(30, 30, 30)
    pdf.cell(0, 8, f"Patient Name : {patient_name}", ln=True)
    pdf.cell(0, 8, f"Disease Tested: {disease}", ln=True)
    pdf.cell(0, 8, f"Report Date   : {datetime.now().strftime('%d-%m-%Y %H:%M:%S')}", ln=True)
    pdf.ln(4)

    # Result
    pdf.set_font("Arial", "B", 14)
    if "Positive" in result or "HAS" in result or "DIABETIC" in result and "NOT" not in result:
        pdf.set_text_color(220, 50, 50)
    else:
        pdf.set_text_color(34, 139, 34)
    pdf.cell(0, 12, f"Prediction Result: {result}", ln=True)
    pdf.ln(4)

    # Divider
    pdf.set_draw_color(200, 200, 200)
    pdf.set_line_width(0.4)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(5)

    # Input Values
    pdf.set_font("Arial", "B", 13)
    pdf.set_text_color(44, 130, 201)
    pdf.cell(0, 10, "Input Parameters", ln=True)
    pdf.set_font("Arial", "", 11)
    pdf.set_text_color(30, 30, 30)
    for label, value in zip(input_labels, inputs):
        pdf.cell(95, 8, str(label), border=1)
        pdf.cell(95, 8, str(value), border=1, ln=True)
    pdf.ln(6)

    # Footer
    pdf.set_font("Arial", "I", 9)
    pdf.set_text_color(150, 150, 150)
    pdf.cell(0, 8, "* This report is generated by an AI model for educational purposes only.", ln=True)
    pdf.cell(0, 8, "* Always consult a certified medical professional for diagnosis and treatment.", ln=True)

    tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".pdf")
    pdf.output(tmp.name)
    return tmp.name

